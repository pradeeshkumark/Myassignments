package week1.day1;

public class Learnoperators {

	public static void main(String[] args) {
		
	//Assignment operator
	int a=10;
	int b=20;
	//Arithmetic operator
		System.out.println("Arithmetic operators Add :"+(a+b));
		System.out.println("Arithmetic operators Subract:"+(a-b));
		System.out.println("Arithmetic operators Multiply:"+(a*b));
		System.out.println("Arithmetic operators Divide:"+(a/b));
		System.out.println("Arithmetic operators Modulo:"+(a%b));
		
	//Comparison operators
	
		System.out.println("Comparison operators less than :"+(a<b));
		System.out.println("Comparison operators greater than:"+(a>b));
		System.out.println("Comparison operators lesser than or equal to:"+(a<=b));
		System.out.println("Comparison operators greater than or equal to:"+(a>=b));
		System.out.println("Comparison operators equal to:"+(a==b));
		System.out.println("Comparison operators not equal to:"+(a!=b));
		
	
	//Logical operators
		System.out.println("Logical operators and :"+(a<b&&a>b));
		System.out.println("Logical operators or :"+(a<b||a>b));
		
		
		
	//Increment Operator
		
		int i=10;
		System.out.println(++i);
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i);
		
		System.out.println(--i);
		System.out.println(i);
		System.out.println(i--);
		System.out.println(i);
		
		
		
	}
}
